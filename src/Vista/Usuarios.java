    private void txtEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEliminarUsuarioActionPerformed
        // TODO add your handling code here:
            if (!"".equals(txtIdUsuario.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar?");
            if (pregunta == 0) {
                int id = Integer.parseInt(txtIdUsuario.getText());
                usuar.EliminarUsuario(id);
                LimpiarTable();
                ListarUsuario();
            }
        }
    }//GEN-LAST:event_txtEliminarUsuarioActionPerformed

